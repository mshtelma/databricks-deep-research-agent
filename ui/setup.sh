        update_env_value "AGENT_CONFIG_PROFILE" "$AGENT_CONFIG_PROFILE" "Agent Endpoint Configuration"
        update_env_value "AGENT_ENDPOINT_NAME" "$AGENT_ENDPOINT_NAME"
