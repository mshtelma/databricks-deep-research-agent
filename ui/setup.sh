    else
        echo "âŒ Invalid option. Using same workspace as default."
        prompt_with_default "Agent Endpoint Name" "${AGENT_ENDPOINT_NAME:-deep-research-agent}" "AGENT_ENDPOINT_NAME"
        update_env_value "AGENT_ENDPOINT_NAME" "$AGENT_ENDPOINT_NAME" "Agent Endpoint Configuration"
