            read -p "Use existing agent token? (y/N): " use_existing_agent
            if [[ ! "$use_existing_agent" =~ ^[Yy]$ ]]; then
                AGENT_DATABRICKS_TOKEN=""
            fi
        fi
        
        if [ -z "$AGENT_DATABRICKS_TOKEN" ]; then
            echo ""
            echo "You can create a Personal Access Token for the agent workspace here:"
            echo "ðŸ“– $AGENT_DATABRICKS_HOST/settings/user/developer/access-tokens"
            echo ""
            read -s -p "Agent Databricks Personal Access Token: " AGENT_DATABRICKS_TOKEN
            echo ""
        fi
        
        prompt_with_default "Agent Endpoint Name" "${AGENT_ENDPOINT_NAME:-deep-research-agent}" "AGENT_ENDPOINT_NAME"
        
        # Update agent configuration in .env.local
        update_env_value "AGENT_DATABRICKS_HOST" "$AGENT_DATABRICKS_HOST" "Agent Endpoint Configuration"
        update_env_value "AGENT_DATABRICKS_TOKEN" "$AGENT_DATABRICKS_TOKEN"
        update_env_value "AGENT_ENDPOINT_NAME" "$AGENT_ENDPOINT_NAME"
        update_env_value "AGENT_CONFIG_PROFILE" ""
        
    elif [ "$agent_choice" = "3" ]; then
        # Different workspace with profile
        echo ""
        echo "ðŸ“‹ Agent Endpoint Profile Configuration"
        echo "--------------------------------------"
        
